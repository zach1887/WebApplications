public class TheOrderOfThings {
    public static void main(String[] args) {
        int number;
        String opinion, size, age, shape, color, origin, material, purpose;
        String noun;
        
        number = 4;
        opinion = "AVERAGE";
        size = "small";
        age = "older";
        shape = "oblong";
        color ="cyan";
        origin="Eastern Asia";
        material = "silver";
        purpose = "neutral good";
        
        noun = "dragons";
        
        //Using the + with strings to concatenate instead of adding
        System.out.println(number  + " " + size +  " " + age + " " 
                + opinion + " " + color  + " " + origin + " " 
                + material + " " + purpose +   " " + noun + ".");
        
        System.out.println(number + " " + size  + " " + age +  " " + opinion 
                + " " + purpose + " " + color  + " " + origin   + " " + material 
                + " " + noun + ".");
        
        
    }
    
}
